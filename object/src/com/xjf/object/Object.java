package com.xjf.object;

public class Object {
	public char pub;
	private char pri;
	protected char pro;
	char def;
	public char getPub() {
		return pub;
	}
	public void setPub(char pub) {
		this.pub = pub;
	}
	public char getPri() {
		return pri;
	}
	public void setPri(char pri) {
		this.pri = pri;
	}
	public char getPro() {
		return pro;
	}
	public void setPro(char pro) {
		this.pro = pro;
	}
	public char getDef() {
		return def;
	}
	public void setDef(char def) {
		this.def = def;
	}
	
	

}

